    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
String nomefornecedorr = txtNome.getText();
String cnpjj = txtCnpj.getText();
String produto = txtProduto.getText();
int qntdd = Integer.parseInt(txtQnt.getText());
String date = txtDate.getText();
String uff = txtUf.getText();



if(nomefornecedorr.isEmpty() || cnpjj.isEmpty() || produto.isEmpty() || date.isEmpty() || uff.isEmpty()){
    
    JOptionPane.showMessageDialog(this,"Preencha todos os campos por favor!");
}


CadastroJPA cj = new CadastroJPA();
cj.setNomefornecedor(nomefornecedorr);
cj.setCnpj(cnpjj);
cj.setProduto(produto);
cj.setQuantidade(qntdd);
String dataTexto = txtDate.getText(); 

try {
    SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
    Date dataConvertida = formato.parse(dataTexto);
    cj.setData(dataConvertida);
} catch (ParseException e) {
    JOptionPane.showMessageDialog(this, "Data inv√°lida! Use o formato dd/MM/yyyy.");
    return;
}


cj.setUf(uff);

CadastroDAO cd = new CadastroDAO();
cd.salvar(cj);

       JOptionPane.showMessageDialog(this,"Pessoa salva com sucesso!");
       
         ListView lv = new ListView();
        lv.carregarTabela();
        
    txtNome.setText("");
    txtCnpj.setText("");
    txtProduto.setText("");
    txtQnt.setText("");
    txtDate.setText("");
    txtUf.setText("");
    
    
    dispose();
    





        
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

