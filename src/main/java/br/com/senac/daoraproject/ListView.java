    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       
        
        int linhaselecionada = jTable2.getSelectedRow();
        
        if(linhaselecionada == -1){
            javax.swing.JOptionPane.showMessageDialog(this,"Selecione uma linha para deletar!");
            
         return ;  
        }
        int confirm = JOptionPane.showConfirmDialog(this,"Tem certeza que quer deletar essa linha?");
        if(confirm != javax.swing.JOptionPane.YES_OPTION){
            return;
            
        } Object idObj = jTable2.getValueAt(linhaselecionada, 0);
    int id = Integer.parseInt(idObj.toString());
    

    CadastroDAO dao = new CadastroDAO();
    boolean sucesso = dao.deletar(id); 
    if (sucesso) {
        javax.swing.JOptionPane.showMessageDialog(this, "Registro deletado com sucesso!");
        carregarTabela(); 
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Erro ao deletar registro.");
    }
        
        
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 try {
        String inicioStr = jTextField1.getText().trim();
        String fimStr = jTextField2.getText().trim();

        if (inicioStr.isEmpty() || fimStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha as duas datas para filtrar.");
            return;
        }

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy", new Locale("pt", "BR"));
        Date dataInicio = sdf.parse(inicioStr);
        Date dataFim = sdf.parse(fimStr);

        if (dataInicio.after(dataFim)) {
            JOptionPane.showMessageDialog(this, "A data inicial deve ser menor ou igual Ã  data final.");
            return;
        }

        CadastroDAO dao = new CadastroDAO();
        List<CadastroJPA> listaFiltrada = dao.buscarPorIntervalo(dataInicio, dataFim);
        atualizarTabela(listaFiltrada);
    } catch (ParseException e) {
        JOptionPane.showMessageDialog(this, "Erro ao converter as datas. Use o formato dd/MM/yyyy.");
        e.printStackTrace();
    }

    }//GEN-LAST:event_jButton1ActionPerformed
   public void carregarTabela(){
        System.out.println("carregando!!!");
    String[] colunas = {"ID","NOME FORNECEDOR","CNPJ","PRODUTO","DATA","QUANTIDADE", "UF"};
    DefaultTableModel modelo = new DefaultTableModel(colunas, 0);
    CadastroDAO cddd = new CadastroDAO();
List<CadastroJPA> lista = cddd.listarTodos();

    for(CadastroJPA c : lista){
    Object[] linha = {
   c.getId(),
   c.getNomefornecedor(),
   c.getCnpj(),
   c.getProduto(),
   c.getData(),
   c.getQuantidade(),
   c.getUf()};
    
    
    modelo.addRow(linha);

}
    jTable2.setModel(modelo);
}
   
   public void atualizarTabela(List<CadastroJPA> lista) {
    String[] colunas = {"ID","NOME FORNECEDOR","CNPJ","PRODUTO","DATA","QUANTIDADE", "UF"};
    DefaultTableModel modelo = new DefaultTableModel(colunas, 0);

    for (CadastroJPA c : lista) {
        Object[] linha = {
            c.getId(),
            c.getNomefornecedor(),
            c.getCnpj(),
            c.getProduto(),
            c.getData(),
            c.getQuantidade(),
            c.getUf()
        };
        modelo.addRow(linha);
    }

    jTable2.setModel(modelo);
}

   
   

